package lesson08.Homework_Figure;

/**
 * Абстрактный класс, описывающий сущность Фигура
 */

public abstract class Figure {

    /**
     * Абстрактный метод для подсчета площади
     */
    public abstract double areaCalculation();

    /**
     * Абстрактный метод для подсчета периметра
     */
    public abstract double perimeterCalculation();

}
